
//4.14 What is the difference between preincrementing and postincrementing a variable?

/*
 In Java, both preincrementing and postincrementing are ways to 
 increase the value of a variable by 1. However, they differ in 
 when the increment operation is applied relative to the current value of the variable.

Preincrementing:
Syntax: ++variable or variable++
In preincrementing, the value of the variable is incremented before 
its value is used in the expression.


Postincrementing:
Syntax: variable++
In postincrementing, the current value of the variable is used in the expression, 
and then the value is incremented.

 */