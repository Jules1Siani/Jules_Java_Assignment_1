
/*Explain what happens when a Java program attempts to divide one integer by another. 
What happens to the fractional part of the calculation? How can you avoid that outcome?*/

/*
When a Java program attempts to divide one integer by another integer 
using the division operator /, the result is also an integer, and any 
fractional part of the calculation is discarded. 
This behavior is known as integer division. 

Here's what happens during integer division:

* Division: The division operation is performed as usual, resulting in a quotient.
* Fractional Part Discarded: Any fractional part of the quotient is discarded,
and only the integer part is retained.
* Result: The result of the division operation is an integer, representing the integer quotient of the division.
 */